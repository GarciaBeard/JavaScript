function soma(){
	// declaração de variável
	var nro1, nro2, resultado;
	// entrada de dados
	// conversão de tipos
	nro1 = parseInt(prompt("Informe um número"));
	nro2 = parseInt(prompt("Informe outro número"));
	// processamento
	resultado = nro1 + nro2;
	// saída de dados
	alert("A soma é " + resultado);
}